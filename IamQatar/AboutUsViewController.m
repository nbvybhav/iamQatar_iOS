//MARK:- API CALL
- (void)getAboutUsContent{

    if([Utility reachable]){

        [ProgressHUD show];
//        AFHTTPRequestOperationManager *manager = [AFHTTPRequestOperationManager manager];
        AFHTTPSessionManager *manager = [[AFHTTPSessionManager alloc] initWithSessionConfiguration:[NSURLSessionConfiguration defaultSessionConfiguration]];
        [manager POST:[NSString stringWithFormat:@"%@%@",parentURL,aboutUs] parameters:nil headers:nil progress:nil success:^(NSURLSessionTask *task, id responseObject)
         {
             NSString *text = [responseObject objectForKey:@"text"];
             if ([text isEqualToString: @"Success!"])
             {
                 NSLog(@"JSON: %@", responseObject);
                 NSString *htmlString = [NSString stringWithFormat:@"%@",[[[responseObject objectForKey:@"value"]valueForKey:@"description"]objectAtIndex:0]];
                 NSString *str = [htmlString stringByAppendingString:[NSString stringWithFormat:@"<style>body{font-family: 'SFUIDisplay-Medium'; font-size:14.0px}</style>"]];
                 NSMutableAttributedString *attributedString = [[NSMutableAttributedString alloc] initWithData:[str dataUsingEncoding:NSUTF8StringEncoding] options:@{ NSDocumentTypeDocumentAttribute: NSHTMLTextDocumentType,NSCharacterEncodingDocumentAttribute: @(NSUTF8StringEncoding) } documentAttributes:nil error:nil];
                 self->_tvAboutUs.attributedText = attributedString;
             }

             [ProgressHUD dismiss];

         } failure:^(NSURLSessionTask *task, NSError *error) {
             NSLog(@"Error: %@", error);
             [ProgressHUD dismiss];
         }];
    }else
    {
        [AlertController alertWithMessage:@"No network connection available!" presentingViewController:self];
    }
}



